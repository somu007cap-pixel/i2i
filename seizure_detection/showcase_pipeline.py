def load_json(path: Path) -> dict:
    with path.open("r", encoding="utf-8") as file_handle:
        return json.load(file_handle)


def valid_detection_results(path: Path) -> bool:
    """Return True only for results generated by the leakage-safe pipeline."""
    if not path.exists():
        return False
    try:
        results = load_json(path)
    except Exception:
        return False
    sensor_policy = results.get("sensor_policy", {})
    label_policy = results.get("label_policy", {})
    run_config = results.get("run_config", {})
    provenance = results.get("model_provenance", {})
    variant_paths = [
        item.get("checkpoint_path")
        for item in provenance.get("variants", {}).values()
        if isinstance(item, dict)
    ]
    checkpoints_present = bool(variant_paths) and all(
        Path(path).exists() for path in variant_paths if path
    )
    expected_window_seconds = _env_float("DETECTION_WINDOW_SECONDS", 5.0)
    expected_stride_seconds = (
        _env_int(
            "DETECTION_STRIDE",
            int(round(expected_window_seconds * 32)),
        )
        / 32.0
    )
    expected_max_sessions = _env_int("DETECTION_MAX_SESSIONS", 80)
    return (
        results.get("pipeline_version") == PIPELINE_VERSION
        and results.get("status") == "completed"
        and results.get("split_strategy") == "patient-level held-out split"
        and sensor_policy.get("synthetic_signals") is False
        and isinstance(results.get("product_allocation_selection"), dict)
        and isinstance(results.get("best_science_model"), dict)
        and isinstance(results.get("best_product_model"), dict)
        and isinstance(results.get("matched_false_alarm_operating_point"), dict)
        and label_policy.get("seizure_duration_seconds")
        == _env_int("SEIZURE_DURATION_SECONDS", 300)
        and label_policy.get("merge_gap_seconds")
        == _env_int("SEIZURE_MERGE_GAP_SECONDS", 0)
        and abs(
            float(label_policy.get("detection_window_seconds", -1.0))
            - expected_window_seconds
        )
        < 1e-6
        and abs(
            float(label_policy.get("detection_stride_seconds", -1.0))
            - expected_stride_seconds
        )
        < 1e-6
        and run_config.get("selected_session_count") == expected_max_sessions
        and (
            provenance.get("retrained_from_scratch_all_variants") is True
            or checkpoints_present
        )
    )


def valid_prediction_results(path: Path) -> bool:
    """Return True only for session-held-out prediction reports."""
    if not path.exists():
        return False
    try:
        results = load_json(path)
    except Exception:
        return False
    dataset_usage = results.get("dataset_usage", {})
    full_counts = dataset_usage.get("full_dataset_counts", {})
    expected_sessions = _env_int("PREDICTION_MAX_SESSIONS", 40)
    train_sessions = results.get("train_sessions", [])
    val_sessions = results.get("val_sessions", [])
    metrics = results.get("metrics", {})
    return (
        results.get("pipeline_version") == PIPELINE_VERSION
        and results.get("status") == "completed"
        and results.get("split_strategy") == "patient-level held-out split"
        and isinstance(metrics, dict)
        and len(train_sessions) + len(val_sessions) == expected_sessions
        and dataset_usage.get("final_metrics_use_full_validation") is True
        and full_counts.get("val_sequences") is not None
        and all(
            isinstance(metrics.get(str(horizon)), dict)
            and metrics[str(horizon)].get("evaluation_split") == "full_validation"
            and metrics[str(horizon)].get("n_val") == full_counts.get("val_sequences")
            for horizon in (300, 900, 1800)
        )
    )


def build_detection_summary(results: dict) -> Path:
    summary_dir = SHOWCASE_DIR / "detection"
    summary_dir.mkdir(parents=True, exist_ok=True)

    standard = results.get("standard_mode", {})
    pro = results.get("pro_mode", {})

    metrics = ["recall", "precision", "f1", "pr_auc", "auc", "balanced_accuracy"]
    standard_values = [float(standard.get(metric, 0.0)) for metric in metrics]
    pro_values = [float(pro.get(metric, 0.0)) for metric in metrics]

    x = np.arange(len(metrics))
    width = 0.35

    fig, ax = plt.subplots(figsize=(10, 6))
    ax.bar(
        x - width / 2,
        standard_values,
        width,
        label="Standard / Watch Only",
        color="#6C757D",
    )
    ax.bar(
        x + width / 2, pro_values, width, label="Pro / Add-on Sensors", color="#2E86AB"
    )
    ax.set_xticks(x)
    ax.set_xticklabels([name.upper().replace("_", "-") for name in metrics])
    ax.set_ylim(0, 1)
    ax.set_ylabel("Score")
    ax.set_title("Detection Quality Metrics")
    ax.legend()
    ax.grid(axis="y", alpha=0.25)

    output_path = summary_dir / "detection_comparison.png"
    fig.tight_layout()
    fig.savefig(output_path, dpi=300, bbox_inches="tight")
    plt.close(fig)

    matched = results.get("matched_false_alarm_operating_point", {})
    if isinstance(matched, dict) and matched.get("standard") and matched.get("pro"):
        matched_metrics = [
            "recall",
            "precision",
            "f1",
            "balanced_accuracy",
            "false_alarms_per_hour",
        ]
        standard_matched = matched["standard"]
        pro_matched = matched["pro"]
        standard_matched_values = [
            float(standard_matched.get(metric, 0.0)) for metric in matched_metrics
        ]
        pro_matched_values = [
            float(pro_matched.get(metric, 0.0)) for metric in matched_metrics
        ]

        fig, axes = plt.subplots(1, 2, figsize=(12, 5))
        score_metrics = matched_metrics[:4]
        score_x = np.arange(len(score_metrics))
        axes[0].bar(
            score_x - width / 2,
            standard_matched_values[:4],
            width,
            label="Standard",
            color="#6C757D",
        )
        axes[0].bar(
            score_x + width / 2,
            pro_matched_values[:4],
            width,
            label="Pro",
            color="#2E86AB",
        )
        axes[0].set_xticks(score_x)
        axes[0].set_xticklabels(
            [name.upper().replace("_", "-") for name in score_metrics]
        )
        axes[0].set_ylim(0, 1)
        axes[0].set_ylabel("Score")
        axes[0].set_title("Matched Alarm Budget Metrics")
        axes[0].legend()
        axes[0].grid(axis="y", alpha=0.25)

        axes[1].bar(
            ["Standard", "Pro"],
            [standard_matched_values[4], pro_matched_values[4]],
            color=["#6C757D", "#2E86AB"],
        )
        axes[1].set_ylabel("False alarms / hour")
        axes[1].set_title("Held-Out Test Alarm Burden")
        axes[1].grid(axis="y", alpha=0.25)

        matched_path = summary_dir / "detection_matched_alarm_budget.png"
