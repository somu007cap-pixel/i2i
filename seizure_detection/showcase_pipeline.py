def _env_int(name: str, default: int) -> int:
    try:
        return int(os.environ.get(name, str(default)))
    except ValueError:
        return default


def _env_float(name: str, default: float) -> float:
    try:
        return float(os.environ.get(name, str(default)))
    except ValueError:
        return default


def format_elapsed(seconds: float) -> str:
    """Format elapsed seconds as a compact human-readable duration."""
    seconds = int(round(seconds))
    hours, remainder = divmod(seconds, 3600)
    minutes, seconds = divmod(remainder, 60)
    if hours:
        return f"{hours}h {minutes}m {seconds}s"
    if minutes:
        return f"{minutes}m {seconds}s"
    return f"{seconds}s"


def timed_step(label: str, func, *args, **kwargs):
    """Run a pipeline step and print its elapsed time."""
    start = time.perf_counter()
    print(f"\n[{label}] Started at {time.strftime('%Y-%m-%d %H:%M:%S')}")
    try:
        return func(*args, **kwargs)
    finally:
        elapsed = time.perf_counter() - start
        print(f"[{label}] Finished in {format_elapsed(elapsed)}")


